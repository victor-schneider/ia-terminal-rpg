package Components;

public class Personagem {
  String name;
  String weapon;
  int level;
  int hp;

  public String getName() {
    return name;
  }

  public String getWeapon() {
    return weapon;
  }

  public int getLevel() {
    return level;
  }

  public int getHp() {
    return hp;
  }
}
